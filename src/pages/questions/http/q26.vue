<p>XSS-атака (Cross-Site Scripting) - это тип атаки на веб-приложения, при
которой злоумышленник внедряет вредоносный JavaScript код в веб-страницы
или приложения, которые просматривают другие пользователи. Когда
пользователь взаимодействует с зараженной страницей, вредоносный код
