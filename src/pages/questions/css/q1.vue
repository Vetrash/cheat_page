      <li>
         <p>Селектор по идентификатору:</p>
         <ul>
            <li>#myElement - выбирает элемент с идентификатором myElement.</li>
         </ul>
      </li>
      <li>
         <p>Селектор по классу:</p>
         <ul><li>.myClass - выбирает все элементы с классом myClass.</li></ul>
      </li>
      <li>
         <p>Селектор потомка:</p>
         <ul><li>.parent .child - выбирает все элементы с классом child, которые являются потомками элемента с классом parent.</li></ul>
      </li>
      <li>
         <p>Селектор псевдокласса:</p>
         <ul><li>a:hover - выбирает ссылки, когда они находятся в состоянии наведения.</li></ul>
      </li>
      <li>
         <p>Селектор атрибута:</p>
         <ul><li>
   [type="text"] - выбирает элементы с атрибутом type и значением text.</li></ul>
      </li>
  </ul>
