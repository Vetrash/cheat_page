  <h2>Что такое селектор</h2>
  <p>Селектор в CSS - это паттерн, который используется для выбора и стилизации определенных элементов на веб-странице.</p>
  <p>Примеры селекторов:</p>
  <ul>
      <li>
         <p>Селектор по типу элемента:</p>
         <ul>
            <li>h1 - выбирает все элементы {{'<h1>'}}.</li>
            <li>p - выбирает все элементы {{'<p>'}}.</li>
