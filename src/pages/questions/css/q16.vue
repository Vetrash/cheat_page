        позиционированием полностью вырывается из потока документа и
        позиционируется относительно его ближайшего родительского элемента с
        нестатическим позиционированием или относительно самого окна браузера.
        Элементы с абсолютным позиционированием могут быть смещены с помощью
        свойств top, right, bottom и left.
      </p>
    </li>
