<template>
  <h2>Какие виды позиционирования знаете</h2>
  <p>В CSS существуют несколько основных видов позиционирования элементов:</p>
  <ul>
    <li>
      <p>
        Static --  Статическое позиционирование: Это значение по умолчанию для всех
