    жизненного цикла компонента, позволяющий выполнять определенный код в
    определенное время. Вот список основных методов:
  </p>
  <ul>
    <li>
      <p>
        <strong>constructor</strong> – первый метод, вызываемый при создании
        компонента.
      </p>
      
        <p>
          Он используется для инициализации состояния и привязки обработчиков
          событий. В функциональных компонентах для аналогичных целей
          используется хук <strong>useState</strong>.
        </p>
      
    </li>
    <li>
      <p>render -- отвечает за рендеринг JSX-разметки и возвращает</p>
      
        <p>содержимое, которое будет выведено на экран.</p>
      
    </li>
    <li>
      <p>
        <strong>componentDidMount</strong> – вызывается сразу после рендеринга
      </p>
      
        <p>
          компонента в DOM. Обычно используется для задач инициализации, таких
