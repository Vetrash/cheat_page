      </p>
      
        <p>
          удалением компонента из DOM. Используется для очистки ресурсов,
          которые были установлены в componentDidMount, например, для удаления
          слушателей событий или отмены таймеров.
        </p>
      
    </li>
  </ul>
  <p>
    Некоторые методы жизненного цикла (componentWillMount,
