<template>
  <h2>Зачем нужны ключи в списках при использовании map()</h2>
  <p>
    Ключи в списках помогают React отслеживать изменения в списке. Когда
    элементы в списке обновляются, удаляются или добавляются, React использует
    ключи для определения того, какие изменения произошли и как обновить DOM.
    Без ключей React не сможет эффективно обновлять список, что может привести к
    снижению производительности. При использовании map() необходимо указывать
    ключи, чтобы React мог связать каждый элемент списка с соответствующей
    записью в массиве. При выборе ключа лучше использовать уникальные
    идентификаторы элементов, чтобы React мог легко определить, какой элемент
    был изменен.
