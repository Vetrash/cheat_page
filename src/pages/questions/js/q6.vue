      </p>
    </li>
    <li>
      <p>
        <strong>classList.contains()</strong>: проверяет, содержит ли элемент указанный класс.
      </p>
    </li>
    <li>
      <p><strong>classList.replace()</strong>: заменяет один класс на другой.</p>
